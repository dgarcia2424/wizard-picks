# ── Bet Tracker tab ───────────────────────────────────────────────────────────
with tab_tracker:
    st.subheader("Bet Tracker — Season Log")
    tracker_df = load_tracker()

    if tracker_df.empty:
        st.info("No bets logged yet.")
    else:
        # Season summary
        if "result" in tracker_df.columns and "profit_loss" in tracker_df.columns:
            wins   = (tracker_df["result"] == "WIN").sum()
            losses = (tracker_df["result"] == "LOSS").sum()
            pushes = (tracker_df["result"] == "PUSH").sum()
            total_pl = pd.to_numeric(tracker_df["profit_loss"], errors="coerce").sum()
            total_bets = wins + losses + pushes
            win_pct = (wins / total_bets * 100) if total_bets > 0 else 0

            c1, c2, c3, c4, c5 = st.columns(5)
            c1.metric("Record", f"{wins}-{losses}-{pushes}")
            c2.metric("Win %", f"{win_pct:.1f}%")
            c3.metric("Total P&L (units)", f"{total_pl:+.2f}")
            c4.metric("Wins", wins)
            c5.metric("Losses", losses)
            st.divider()

        st.dataframe(tracker_df, use_container_width=True, hide_index=True)

# ── Footer ────────────────────────────────────────────────────────────────────
st.divider()
st.caption(f"Last refreshed: {date.today().strftime('%B %d, %Y')} · Data auto-refreshes every 5 minutes")
